
namespace US.OpenServer
{
    /// <summary>
    /// Enum of log message priorities.
    /// </summary>
    public enum Level
    {
        /// <summary>
        /// Specifies a debug message.
        /// </summary>
        Debug,
        /// <summary>
        /// Specifies an information message.
        /// </summary>
        Info,
        /// <summary>
        /// Specifies a notice message.
        /// </summary>
        Notice,
        /// <summary>
        /// Specifies a critical message.
        /// </summary>
        Critical,
        /// <summary>
        /// Specifies an error message.
        /// </summary>
        Error
    }
}
